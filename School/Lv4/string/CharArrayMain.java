package lang.string;

public class CharArrayMain {
    public static void main(String[] args) {
        //char을 이용해서 'hello'를 출력해라.
        //String을 이용해서 'hello'를 출력해라.
        String str = "hello";

        char[] ch = {'h', 'e', 'l', 'l', 'o'};

        System.out.println(str);
        System.out.println(ch);
    }
}
